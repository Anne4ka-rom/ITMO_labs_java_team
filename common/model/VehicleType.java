package common.model; // класс находится в папке model

/**
 * Enum типов транспортных средств
 * Определяет возможные типы транспорта в коллекции
 * 
 * @author Anni
 * @version 1.0
 */
public enum VehicleType { // объявляем перечисление (enum) с константами типов транспорта
    CAR, // машина
    SUBMARINE, // подлодка
    BICYCLE, // велосипед
    HOVERBOARD; // летающий скейт

    /**
     * Возвращает строку со всеми доступными типами транспорта
     * 
     * @return строка с типами через запятую
     */
    public static String getTypes() { // объявляем публичный статический метод
        StringBuilder sb = new StringBuilder(); // инициализируем изменяемую строку для сборки результата
        for (VehicleType type : values()) { // проходимся по всем значениям перечисления
            sb.append(type.name()).append(", "); // добавляем название транспортного средства и разделитель в буфер
        }
        return sb.substring(0, sb.length() - 2); // удаляем лишние запятую и пробел в конце
    }
}