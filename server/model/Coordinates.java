package server.model; // класс находится в папке model

import java.io.Serializable; // импорт интерфейса Serializable для возможности сериализации объекта (передачи по сети и сохранения в файл)

/**
 * Класс для хранения и управления координатами транспортного средства
 * Обеспечивает валидацию данных и контролируемый доступ к полям
 * Реализует интерфейс {@link Serializable} для возможности передачи объекта по сети
 * 
 * @author Anni
 * @version 2.0
 * @since 1.0
 * @see Serializable
 */
public class Coordinates implements Serializable { // объявляем публичный класс Coordinates, который представляет координаты транспортного средства
    private static final long serialVersionUID = 1L; // // уникальный идентификатор версии класса для сериализации
    private Double x; // приватное поле с координатой x типа Double (оберточный класс для double)
    private int y; // приватное поле с координатой y типа int

    /**
     * Конструктор по умолчанию
     */
    public Coordinates() {} // пустой конструктор по умолчанию, позволяет создавать объект Coordinates без начальных значений, с последующей установкой через сеттеры

    /**
     * Конструктор с параметрами
     * 
     * @param x координата X (не null, <= 636)
     * @param y координата Y
     * @throws IllegalArgumentException если x не прошел валидацию
     */
    public Coordinates(Double x, int y) { // конструктор с параметрами для создания объекта с начальными значениями координат
        setX(x); // вызов сеттера для x, чтобы применить валидацию значения
        this.y = y; // присвоение y (без валидации, так как для него ограничений нет)
    }

    /**
     * Возвращает координату X
     * 
     * @return координата X
     */
    public Double getX() { // геттер для поля x
        return x; // возвращает текущее значение x для чтения
    }

    /**
     * Устанавливает координату X
     * 
     * @param x координата X (не null, <= 636)
     * @throws IllegalArgumentException если x null или > 636
     */
    public void setX(Double x) { // сеттер для поля x с валидацией входных данных
        if (x == null) { // проверка на null
            throw new IllegalArgumentException("X не может быть null"); // если x -- null, выбрасываем исключение IllegalArgumentException
        }
        if (x > 636) { // проверка на максимальное значение поля x
            throw new IllegalArgumentException("X должен быть <= 636"); // если x > 636, выбрасываем исключение IllegalArgumentException
        }
        this.x = x; // если обе проверки пройдены успешно, значение присваивается полю объекта
    }
    

    /**
     * Возвращает координату Y
     * 
     * @return координата Y
     */
    public int getY() { // геттер для поля y
        return y; // возвращает текущее значение y
    }

    /**
     * Устанавливает координату Y
     * 
     * @param y координата Y
     */
    public void setY(int y) { // сеттер для поля y
        this.y = y; // прямое присвоение значения полю объекта
    }

    /**
     * Возвращает строковое представление координат
     * 
     * @return строка вида "(x, y)", например: (100.5, 200)
     */
    @Override // аннотация Override указывает, что метод переопределяет метод суперкласса (Object)
    public String toString() { // переопределяем метода toString() для строкового представления объекта
        return "(" + x + ", " + y + ")"; // возвращает строку с координатами транспортного средства
    }
}